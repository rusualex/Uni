package Domain;

public class StudentGrade {
    Integer id;
    String name;
    Float grade;

    public StudentGrade(Integer id, String name, Float grade) {
        this.id = id;
        this.name = name;
        this.grade = grade;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getGrade() {
        return grade;
    }

    public void setGrade(Float grade) {
        this.grade = grade;
    }

    @Override
    public String toString() {
        return id +
                ", " + name +
                ", " + grade;
    }
}
